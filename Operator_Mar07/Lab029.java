package Operator_Mar07;

public class Lab029 {
    public static void main(String[] args) {
        //Increament Operator
        //Pre and Post
        int a=10;
        a=a+1;
        a+=1;
        a++; // Post Increment -> a=a+1
        System.out.println(a);
        ++a; //Pre increment -> a = a+1
        System.out.println(++a);


    }
}
