package Loops;

public class Lab055 {
    public static void main(String[] args) {
        //Conditions - check for some condition, true or false
        //If,If Else, If Else-If,Else(No Break is used here in conditions),Switch(Break can be used)
        //Loops - Do perform  a task multiple times
        //For, While, Do while(Break and continue)


        //For Loop
        //Use to perform a task multiple task
        //Based on condition
        //for(A,B,C)
        //A->Initialization ->JVM what variable, where you are starting point
        //B-> Condition - When you want this to stop
        //C->Increment/Decrement

        //A-> i=1
        //B-> i<=10;
        //C-> i++;
        //D-> which code(what you want to print as output)

        for(int i=1;i<=10; i++)
        {
            System.out.println("Keerti" +i);
        }
    }
}
